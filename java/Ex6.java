//SWAPPING OF TWO NUMBERS//
public class Ex6{
    public static void main(final String []args){
    int num1=3 ; int num2 = 5; int temp=0;
    System.out.println("The value1 before Swapping ::"+num1 );
    System.out.println("The value2 before Swapping ::"+num2 );
     temp=num1;
     num1=num2;
     num2=temp;
     System.out.println("The value1 after Swapping are ::: " +num1 );
     System.out.println("The value2 after Swapping are ::: " +num2 );
    }
    
}